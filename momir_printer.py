# ---------------- ESC-INPUT ----------------
def esc_input(prompt="> "):
    """Get input from user, ESC returns None"""
    print(prompt, end="", flush=True)
    buf = ""
    fd = sys.stdin.fileno()
    old = termios.tcgetattr(fd)
    try:
        tty.setraw(fd)
        while True:
            ch = sys.stdin.read(1)
            if ch == "\x1b":  # ESC key
                print("\nESC pressed! Returning to main menu.")
                return None
            elif ch in ("\r", "\n"):
                print()
                return buf
            elif ch == "\x7f":  # Backspace
                if buf:
                    buf = buf[:-1]
                    sys.stdout.write("\b \b")
            else:
                buf += ch
                sys.stdout.write(ch)
                sys.stdout.flush()
    finally:
        termios.tcsetattr(fd, termios.TCSADRAIN, old)

# ---------------- NETWORK ----------------
def connect_to_wifi():
    for net in WIFI_NETWORKS:
        try:
            print(f"Trying WiFi: {net['ssid']}")
            subprocess.run(
                ["nmcli", "dev", "wifi", "connect", net["ssid"], "password", net["password"]],
                check=True, stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL
            )
            print(f"Connected to {net['ssid']}")
            return True
        except:
            pass
    return False

